# ── Seeding function ──────────────────────────────────────────────────────────

async def seed_ntd() -> None:
    from app.core.aitunnel import get_embeddings_batch
    from app.modules.ntd.models import NTDClause, NTDDocument

    async with SessionFactory() as db:
        for doc_data in DOCUMENTS:
            code = doc_data["code"]

            # Skip if already exists
            existing = await db.execute(
                select(NTDDocument).where(NTDDocument.code == code)
            )
            if existing.scalar_one_or_none():
                print(f"  SKIP {code} (already exists)")
                continue

            doc = NTDDocument(
                code=code,
                title=doc_data["title"],
                doc_type=doc_data["doc_type"],
                version=doc_data.get("version"),
                effective_date=doc_data.get("effective_date"),
                s3_key=None,
            )
            db.add(doc)
            await db.flush()

            raw_clauses = CLAUSES_BY_CODE.get(code, [])
            if not raw_clauses:
                print(f"  {code}: no clauses defined, skipping clause insert")
                await db.commit()
                continue

            texts = [c[2] for c in raw_clauses]
            print(f"  {code}: embedding {len(texts)} clauses …")
            embeddings = await get_embeddings_batch(texts)

            for (number, title, clause_text), emb in zip(raw_clauses, embeddings):
                clause = NTDClause(
                    document_id=doc.id,
                    clause_number=number,
                    title=title,
                    text=clause_text,
                    page_number=None,
                    embedding=emb,
                )
                db.add(clause)

            await db.flush()

            # Update FTS vectors
            await db.execute(
                text(
                    "UPDATE ntd.clauses SET fts_vector = to_tsvector('russian', "
                    "coalesce(title, '') || ' ' || text) WHERE document_id = :doc_id"
                ),
                {"doc_id": str(doc.id)},
            )
            await db.commit()
            print(f"  OK  {code}: {len(raw_clauses)} clauses indexed")

    print("NTD seed complete.")


if __name__ == "__main__":
    asyncio.run(seed_ntd())

